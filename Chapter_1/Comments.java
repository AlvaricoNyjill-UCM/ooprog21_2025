public class Comments {
    public static void main(String[] args) {
        
        // This is a single-line comment
        System.out.println("Hello, this line is explained with a single-line comment.");
        
        /* 
         * This is a multi-line comment.
         * It can span multiple lines and is often used
         * for explaining longer sections of code.
         */
        System.out.println("This line is explained with a multi-line comment.");
        
        /**
         * This is a documentation comment.
         * It is used to describe classes, methods, and variables.
         * Tools like Javadoc use these comments to generate documentation.
         */
        System.out.println("This line is explained with a documentation comment.");
    }
}